"""
变量的格式化输出
%s  字符串
%d  整数 %06d 显示六位(0后面加位数6) 不足6位 0补全
%f  浮点数 %.02f 小数点后显示两位
%%  输出%
"""

name = "小明"
print("用户名称:%s" % name)

a = 12345
print("整数:%d" % a)
print("整数:%010d" % a)
print("整数:%s" % a)

b = 12.345
print("浮点数:%.02f" % b)

"""多变量格式化输出"""
print("用户:%s  整数:%d  浮点数:%f" % (name, a, b))

"""百分号"""
c = 0.523
print("百分之:%.2f%%" % (c * 100))
